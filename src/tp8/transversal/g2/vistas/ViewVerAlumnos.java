    private void mostrarAlumnos(AlumnoData ad){
        this.ad = ad;
        dtm = (DefaultTableModel) jtAlumnos.getModel();
        dtm.setRowCount(0);
        if (!cbVerInactivos.isSelected()){ 
        for (Alumno a : ad.obtenerAlumnos()){
                if(a.isActivo()){
                String []row = new String[4];
                row[0] = a.getLegajo()+"";
                row[1] = a.getNombre();
                row[2] = a.getApellido();
                row[3]= "Activo";
                dtm.addRow(row);
                jtAlumnos.setModel(dtm);
                }
                
            }
        } else { 
            for (Alumno a : ad.obtenerAlumnos()){
                String []row = new String[4];
                row[0] = a.getLegajo()+"";
                row[1] = a.getNombre();
                row[2] = a.getApellido()+"";
                if(a.isActivo())
                    row[3]= "Activo";
                else
                    row[3]= "Inactivo";
                dtm.addRow(row);
                jtAlumnos.setModel(dtm);
            }
        }
    }
       

